package org.jnuLib.data;
public class GuanCangInfo {

	private String num=null;//序号
	private String Booknum=null;//索书号
	private String guancangdi=null;//馆藏地
	private String zhuangtai=null;//书本状态
	
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getBooknum() {
		return Booknum;
	}
	public void setBooknum(String booknum) {
		Booknum = booknum;
	}
	public String getGuancangdi() {
		return guancangdi;
	}
	public void setGuancangdi(String guancangdi) {
		this.guancangdi = guancangdi;
	}
	public String getZhuangtai() {
		return zhuangtai;
	}
	public void setZhuangtai(String zhuangtai) {
		this.zhuangtai = zhuangtai;
	}


	
}
